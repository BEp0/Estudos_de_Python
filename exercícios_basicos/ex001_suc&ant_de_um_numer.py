# Pedir um número e dizer seu sucessor e antecessor
numero = int(input('Digite um número inteiro:')) # pede o número
# Calcular o sucessor e antecessor...
suc = numero + 1 # Sucessor
ant = numero - 1 # Antecessor

# Dizer o resultado...
print('Seu número foi {0}, o antecessor desse número é {1} e o sucessor é {2}'.format(numero, ant, suc)) 

